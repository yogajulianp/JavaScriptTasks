/* 
> lebih dari
< kurang dari
>= lebih dari sama dengan
<= kurang dari sama dengan
== sama dengan tapi boleh beda tipe
=== sama dengan dan harus tipe sama juga
!= tidak sama dengan
!== tida sama dengan dan tidak sama tipe
*/

let result = 5;

result = 5 == 5;
console.log(result);
result = 5 == "5";
console.log(result);
result = 5 === 5;
console.log(result);
result = 5 === "5";
console.log(result);

result = 5 > 10;
console.log(result);

result = 5 < 10;
console.log(result);

result = 5 != 10;
console.log(result);

result = 5 !== 10;
console.log(result);
